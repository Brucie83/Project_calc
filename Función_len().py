#Función len()
#Esta función sirve para saber la longitud de una cadena de caracteres o la cantidad de elementos en un objeto iterable. Su uso más común es para obtener la cantidad de elementos en una lista, tupla, cadena, diccionario u otro tipo de estructura iterable.
#Opción 1
print("Hola tiene", len("Hola"), "caracteres.")

#Opción 2
longitud = len("La Geekipedia")

print("La Geekipedia tiene", longitud, "caracteres.")

